namespace MZikmund.Toolkit.WinUI.Services;

/// <summary>
/// Represents the result of a confirmation dialog.
/// </summary>
public enum ConfirmationResult
{
	/// <summary>
	/// The user confirmed the action.
	/// </summary>
	Confirmed,

	/// <summary>
	/// The user denied the action.
	/// </summary>
	Denied,
}
